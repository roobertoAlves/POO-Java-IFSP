public class EstruturasCondicionaisOperadorTernario {

    public static void main( String[] args ) {
    
        int n = 20;
        
        // n é par? se sim, retorna 1, caso contrário, retorna 0
        int v = n % 2 == 0 ? 1 : 0;
        
        System.out.println( v );
    
    }
    
}